class Consonant
{
 public static void main(String args[])
 {
  char letter='B';
  if(letter!='A'&&letter!='E'&&letter!='I'&&letter!='O'&&letter!='U')
   System.out.println("The given character" +letter+ "is a consonant");  
  else
   System.out.println("The given character" +letter+ "is an vowel");
 }
}
 