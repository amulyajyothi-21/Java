class Loop
{
 public static void main(String args[])
 {
  int start=1;
  System.out.println("Forward Iteration");
  while(start<=10)
  {
   System.out.println(start);
   start++;
  }
  System.out.println("=============================================");
  System.out.println("Backward Iteration");
  int number=10;
  while(number>=1)
  {
   System.out.println(number);
   number--;
  }
  
 }
}
   
   