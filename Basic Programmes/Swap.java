class Swap {
    public static void main(String[] args) 
    {
        int c=10;
        int d=20;
        int e=0;
        System.out.println("Before Swapping");

        System.out.println(c);
        System.out.println(d);
        e=c;//temp=10
        c=d;//num1=20;
        d=e;//num2=10;
        System.out.println("After Swapping");
        System.out.println(c);
        System.out.println(d);
        
    }
}